// Таблиця множення за допомогою циклу for

const someNumber = 6;

for (let i = 1; i <= 10; i++) {
    console.log(`${someNumber} * ${i} = ${someNumber * i}`);
}

// Таблиця множення за допомогою циклу while

const otherNumber= 8;
let i = 1;
while (i <= 10) {
    console.log(`${otherNumber} * ${i} = ${otherNumber * i}`);
    i++;
}